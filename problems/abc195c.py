N = int(input())

if N < 10 ** 3:
    ans = 0
elif N < 10 ** 6:
    ans = (N - (10 ** 3)) * 1 + 1
elif N < 10 ** 9:
    ans = (10 ** 6 - (10 ** 3)) * 1 + 1 + (N - (10 ** 6)) * 2 + 1
elif N < 10 ** 12:
    ans = (
        (10 ** 6 - (10 ** 3)) * 1
        + 1
        + (10 ** 9 - (10 ** 6)) * 2
        + 1
        + (N - (10 ** 9)) * 3
        + 1
    )
elif N < 10 ** 15:
    ans = (
        (10 ** 6 - (10 ** 3)) * 1
        + 1
        + (10 ** 9 - (10 ** 6)) * 2
        + 1
        + (10 ** 12 - (10 ** 9)) * 3
        + 1
        + (N - (10 ** 12)) * 4
        + 1
    )
elif N < 10 ** 18:
    ans = (
        (10 ** 6 - (10 ** 3)) * 1
        + 1
        + (10 ** 9 - (10 ** 6)) * 2
        + 1
        + (10 ** 12 - (10 ** 9)) * 3
        + 1
        + (10 ** 15 - (10 ** 12)) * 4
        + 1
        + (N - (10 ** 15)) * 4
        + 1
    )
print(ans)
