def prime_factorize(n):
    a = []
    while n % 2 == 0:
        a.append(2)
        n //= 2
    f = 3
    while f * f <= n:
        if n % f == 0:
            a.append(f)
            n //= f
        else:
            f += 2
    if n != 1:
        a.append(n)
    return a


N = int(input())
if N == 1:
    exit(print(0))

A = prime_factorize(N)
c = Counter(A)
ans = 0
for k, v in c.items():
    i = 1
    while v - i >= 0:
        ans += 1
        v -= i
        i += 1

print(ans)
